package day05Oparators;

public class Decrement02 {

	public static void main(String[] args) {
		// int data tipinde bir variable oluþturun bu variable nin deðerini 
		//çarpma ve bölme kullanarak arttýrýn ve azaltýn
		
		
		int num1=12;
		//uzun yol ile çarpma yaparak increment
		num1=num1*3;
		System.out.println(num1);
		//kýsa yol
		num1*=3;
		System.out.println(num1);
		
		
		//uzun yol ile bölme yaparak decrement
		num1=num1/9;
		System.out.println(num1);
		num1/=2;
		System.out.println(num1);
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
