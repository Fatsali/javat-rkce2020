package day05Oparators;

public class Decrement01 {

	public static void main(String[] args) {
		// int data tipinde 1 variable  oluşturun
		//bu veriablenin değerini 3 yöntemi de kullanarak her defasında 1 azaltın
		
		
		int num1=12;
		num1=num1-1;
		System.out.println(num1);
		num1-=1;
		System.out.println(num1);
		num1--;
		System.out.println(num1);
		
		
		
		

	}

}
