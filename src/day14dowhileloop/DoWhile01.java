package day14dowhileloop;

public class DoWhile01 {

	public static void main(String[] args) {
		
		int num=1;
		do {
			System.out.println(num);
			num++;
			
		}while(num<4);
		
		//10 ile 20 arasýndaki tek tam sayýlarý ekrana do while loop ile yazdýrýn
		
		int num2=11;
		do {
			
			System.out.println(num2);
			num2=num2+2;
		}while(num2<20);
				
		
		

	}

}
