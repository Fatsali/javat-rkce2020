package day01variables;

public class Variables01 {
	
	public static void main(String[]args) {
		
		int maas=3000;
		
		char harf='S';//CHAR lara deðer atamasý yaparken tek týrnak kullan.
		
		System.out.println(maas);
		System.out.println(harf);
		
		int sayi=123;
		
		System.out.println(sayi);
		
		boolean isOld=true;// boolean lar için sadece true veya false deðerini kullanabilirim
		
		System.out.println(isOld);
		
		boolean isNew=false;
		
		System.out.println(isNew);
		
		byte derinlik=-123;// -128 den 127 e kadar =8 bit
		
		System.out.println(derinlik);
		
		short sirnakNufus=28000;
		System.out.println(sirnakNufus);
		
		long hucreSayisi=123344559888888l;// integer sýnýrýný aþan long variable larda en sona "l" veya "L" koymalýyýz.
		System.out.println(hucreSayisi);
		
		double pi=3.14577577575775;// java bütün ondalýk kesirleri otomatik olarak double olarak kabul eder. 
		System.out.println(pi);// Default value=0.0
		
		float para=5.25F;// eðer bir ondalýk kesri float yapmak isterseniz sonuna f yada F konulmalýdýr.
		System.out.println(para);//Default value=0.0f
		
		String ogrenciÝsmi="Ali Can";
		System.out.println(ogrenciÝsmi);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
